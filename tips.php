        <div class="content-section">
            <p>Social media plays a significant role in teenagers' lives, offering a space for communication, creativity, and self-expression. However, it also presents challenges, including privacy risks, cyberbullying, and screen time concerns. As a parent, you play a vital role in guiding your teen toward responsible and safe social media usage. Here are some top tips and strategies to help you support your teen in navigating social media wisely.</p>

            <div class="tips-section">
                <h3>1. Open Communication is Key</h3>
                <img src="photos/guidance/image2.jfif" alt="Open Communication" class="tip-image">
                <p>Talking openly and regularly with your teen about their social media use is one of the best ways to keep them safe online.</p>
                <ul class="values-list">
                    <li>Make discussions about social media a regular part of your conversations.</li>
                    <li>Stay informed about the latest apps, trends, and online risks.</li>
                    <li>Create a safe space where your teen feels comfortable sharing their online experiences.</li>
                </ul>
            </div>

            <div class="tips-section">
                <h3>2. Set Boundaries and Screen Time Limits</h3>
                <img src="photos/guidance/image3.png" alt="Screen Time Management" class="tip-image">
                <p>Setting clear boundaries helps teens balance their screen time with other activities.</p>
                <ul class="values-list">
                    <li>Establish rules for screen time, including no-phone zones like during meals or before bed.</li>
                    <li>Use parental controls to monitor and manage your teen's device usage.</li>
                    <li>Encourage offline activities, such as sports, hobbies, and family time.</li>
                </ul>
            </div>

            <div class="tips-section">
                <h3>3. Teach Privacy and Safety Skills</h3>
                <img src="photos/guidance/image3.jfif" alt="Privacy Awareness" class="tip-image">
                <p>Ensuring your teen understands privacy settings and safe online behavior is crucial.</p>
                <ul class="values-list">
                    <li>Educate your teen on the importance of privacy settings and adjusting them to keep information secure.</li>
                    <li>Encourage the use of strong, unique passwords and warn against sharing them.</li>
                    <li>Remind your teen to avoid interactions with strangers and never share personal information.</li>
                </ul>
            </div>

            <div class="tips-section">
                <h3>4. Be Aware of Cyberbullying</h3>
                <img src="photos/guidance/image4.jfif" alt="Privacy Awareness" class="tip-image">
                <p>Cyberbullying can have serious effects on your teen’s mental health.</p>
                <ul class="values-list">
                    <li>Recognize signs of cyberbullying, such as changes in mood or reluctance to use devices.</li>
                    <li>Encourage your teen to report bullying and assure them of your support.</li>
                    <li>Familiarize yourself with the reporting tools on social media platforms.</li>
                </ul>
            </div>

            <div class="tips-section">
                <h3>5. Model Positive Behavior</h3>
                <img src="photos/guidance/image5.png" alt="Privacy Awareness" class="tip-image">
                <p>Your online behavior sets an example for your teen.</p>
                <ul class="values-list">
                    <li>Demonstrate healthy social media habits, like putting your phone away during family time.</li>
                    <li>Show how to engage positively and manage negative content.</li>
                </ul>
            </div>

            <div class="tips-section">
                <h3>6. Discuss Digital Footprints and Online Reputation</h3>
                <img src="photos/guidance/image6.png" alt="Privacy Awareness" class="tip-image">
                <p>Help your teen understand that what they post online can have long-term impacts.</p>
                <ul class="values-list">
                    <li>Encourage them to think twice before posting anything that could be regrettable later.</li>
                    <li>Explain how online behavior can affect their future opportunities.</li>
                </ul>
            </div>

            <div class="tips-section">
                <h3>7. Encourage Critical Thinking</h3>
                <img src="photos/guidance/image7.png" alt="Privacy Awareness" class="tip-image">
                <p>Teach your teen to question what they see and recognize online pressures.</p>
                <ul class="values-list">
                    <li>Discuss the prevalence of misinformation and the importance of fact-checking.</li>
                    <li>Talk about the unrealistic portrayals often seen on social media.</li>
                </ul>
            </div>

             <div class="tips-section">
            <h3>8. Promote Positive Online Behavior</h3>
            <img src="photos/guidance/image8.png" alt="Positive Online Behavior" class="tip-image">
            <p>Encourage respectful communication and the importance of kindness online.</p>
            <ul class="values-list">
                <li>Discuss the impact of cyberbullying and how to handle negative interactions.</li>
                <li>Model positive online behavior and emphasize the value of empathy in digital spaces.</li>
                <li>Teach your teen to report and block inappropriate content or users.</li>
            </ul>
        </div>
			<div class="tips-section">
            <h3>9. Stay Involved and Informed</h3>
            <img src="photos/guidance/image9.png" alt="Parents Staying Involved" class="tip-image">
            <p>Keep up with the evolving landscape of social media by staying involved in your teen’s online world.</p>
            <ul class="values-list">
                <li>Follow or friend your teen on social media (with their consent) to stay connected.</li>
                <li>Participate in their digital activities occasionally to understand their interests and challenges.</li>
                <li>Encourage them to share new apps or trends they find interesting with you.</li>
            </ul>
        </div>
            <div class="tips-section">
                <h3>10. Know When to Step In</h3>
                <img src="photos/guidance/image10.png" alt="Privacy Awareness" class="tip-image">
                <p>Monitoring with balance and knowing when to intervene is crucial.</p>
                <ul class="values-list">
                    <li>Use tools like Family Pairing to manage your teen’s activity while respecting their privacy.</li>
                    <li>If you notice signs of distress, consider seeking professional help from a counselor.</li>
                </ul>
            </div>

               <div class="conclusiontip-section">
        <h3>Conclusion</h3>
        <p>Supporting your teen’s social media journey involves staying informed, setting boundaries, and maintaining open lines of communication. By working together, you can help your teen make positive, safe, and informed choices online.</p>
