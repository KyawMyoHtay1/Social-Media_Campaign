<section class="safety-tips">
    <h2>How to Stay Safe Online</h2>
    <p>Explore comprehensive tips and strategies to protect yourself while navigating social media platforms:</p>
    <ul class="values-list">
        <li><strong>Use Strong, Unique Passwords:</strong> A strong password should include a combination of letters, numbers, and special characters. Avoid using easily guessable information like birthdays or pet names.</li>
        <li><strong>Enable Two-Factor Authentication (2FA):</strong> Add an extra layer of security to your accounts by enabling 2FA, where you’ll need a code sent to your phone or email in addition to your password.</li>
        <li><strong>Be Cautious with Personal Information:</strong> Avoid oversharing personal details like your address, phone number, or even your daily routine. Always be aware of who can see the information you're posting.</li>
    </ul>
    <p>By following these guidelines, you can significantly reduce the risks of online threats and protect your personal information while enjoying social media.</p>
    <br>
    
    <h2>Be Aware of Phishing Scams</h2>
    <p>Phishing scams are attempts to trick you into providing personal information. Here are some ways to protect yourself:</p>
    <ul class="values-list">
        <li><strong>Verify Email Senders:</strong> Always check the sender's email address before clicking on links. If it looks suspicious, do not engage.</li>
        <li><strong>Look for Red Flags:</strong> Be cautious of emails or messages that create a sense of urgency, request personal information, or contain unexpected attachments.</li>
        <li><strong>Report Suspicious Activity:</strong> If you suspect a phishing attempt, report it to your email provider and avoid interacting with the sender.</li>
    </ul>
    <p>Staying vigilant against phishing scams is essential to protect your personal and financial information.</p>
    <br>

    <h2>Adjust Your Privacy Settings</h2>
    <p>Take control of your online presence by adjusting privacy settings on social media platforms:</p>
    <ul class="values-list">
        <li><strong>Limit Profile Visibility:</strong> Adjust your account settings to ensure that only approved friends or followers can see your posts and personal information.</li>
        <li><strong>Review App Permissions:</strong> Regularly check which apps have access to your data and revoke permissions for any that you no longer use.</li>
        <li><strong>Be Mindful of Location Sharing:</strong> Turn off location services on apps unless necessary, and avoid sharing your location in real-time.</li>
    </ul>
    <p>By customizing your privacy settings, you can enhance your online safety and reduce the likelihood of unwanted attention.</p>
</section>


